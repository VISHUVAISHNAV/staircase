m.begin_fill()
m.right(90)
m.forward(300)
m.right(90)
m.forward(7)
m.right(90)
m.forward(300)
m.right(90)
m.forward(7)
m.end_fill()
m.forward(190)
m.right(90)
m.fillcolor("orange")
m.begin_fill()
m.forward(40)
m.right(90)
m.forward(190)
m.right(90)
m.forward(40)
m.end_fill()
m.backward(80)
m.right(90)
m.forward(190)
m.left(90)
m.forward(40)
